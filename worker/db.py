def _parse_timestamp(value: str | None) -> datetime | None:
    if not value:
        return None
    try:
        parsed = datetime.fromisoformat(value.replace("Z", "+00:00"))
    except ValueError:
        return None
    if parsed.tzinfo is None:
        return parsed.replace(tzinfo=timezone.utc)
    return parsed.astimezone(timezone.utc)


def _process_is_alive(pid: int) -> bool:
    if pid <= 0:
        return False
    if pid == os.getpid():
        return True
    if os.name == "nt":
        try:
            result = subprocess.run(
                ["tasklist", "/FI", f"PID eq {pid}", "/FO", "CSV", "/NH"],
                capture_output=True,
                timeout=5,
                check=False,
            )
        except (OSError, subprocess.SubprocessError):
            return True
        output = (result.stdout or b"").strip()
        if not output:
            return False
        return str(pid).encode("ascii") in output
    try:
        os.kill(pid, 0)
    except ProcessLookupError:
        return False
    except PermissionError:
        return True
    return True


def mark_stale_job_runs(
    conn: Any,
    stale_after_seconds: int = JOB_STALE_AFTER_SECONDS,
    legacy_stale_after_seconds: int = LEGACY_JOB_STALE_AFTER_SECONDS,
) -> dict[str, int]:
    now = datetime.now(timezone.utc)
    now_text = now.isoformat(timespec="seconds")
    rows = conn.execute(
        """
        SELECT id, job_type, started_at, message, pid, heartbeat_at, meta_json
        FROM job_runs
        WHERE status = 'running'
        ORDER BY id
        """
    ).fetchall()
    marked = 0
    for row in rows:
        meta = from_json(row["meta_json"], {})
        if not isinstance(meta, dict):
            meta = {}
        if meta.get("worker_job"):
            continue
        pid = int(row["pid"] or 0)
        started = _parse_timestamp(row["started_at"])
        reason = ""
        if pid:
            if not _process_is_alive(pid):
                reason = f"process {pid} is no longer running"
        elif started and now - started > timedelta(seconds=legacy_stale_after_seconds):
            reason = f"legacy running job older than {legacy_stale_after_seconds} seconds"
        if not reason:
            continue
        meta["stale"] = {
            "marked_at": now_text,
            "reason": reason,
        }
        previous_message = str(row["message"] or "").strip()
        message = f"Marked stale: {reason}"
        if previous_message:
            message = f"{message}; previous message: {previous_message[:500]}"
        conn.execute(
            """
            UPDATE job_runs
            SET status = 'failed',
                finished_at = ?,
                message = ?,
                heartbeat_at = ?,
                meta_json = ?
            WHERE id = ? AND status = 'running'
            """,
            (now_text, message, now_text, to_json(meta), int(row["id"])),
        )
        marked += 1
    if marked:
        conn.commit()
    return {"stale_jobs_checked": len(rows), "stale_jobs_marked": marked}


@contextmanager
def job_run(conn: Any, job_type: str) -> Iterator[int]:
    started = utc_now()
    cur = conn.execute(
        """
        INSERT INTO job_runs(job_type, status, started_at, pid, heartbeat_at)
        VALUES (?, 'running', ?, ?, ?)
        """,
        (job_type, started, os.getpid(), started),
    )
    job_id = int(cur.lastrowid)
    conn.commit()
    try:
        yield job_id
    except Exception as exc:
        try:
            conn.rollback()
        except Exception:
            pass
        try:
            conn.execute(
                """
                UPDATE job_runs
                SET status = 'failed', finished_at = ?, message = ?, heartbeat_at = ?
                WHERE id = ?
                """,
                (utc_now(), str(exc), utc_now(), job_id),
            )
            conn.commit()
        except Exception:
            try:
                conn.rollback()
            except Exception:
                pass
        raise
    else:
        conn.execute(
            """
            UPDATE job_runs
            SET status = 'completed', finished_at = ?, heartbeat_at = ?
            WHERE id = ?
            """,
            (utc_now(), utc_now(), job_id),
        )
        conn.commit()



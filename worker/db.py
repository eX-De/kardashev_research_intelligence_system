def clean_unicode(value: Any) -> Any:
    if isinstance(value, str):
        try:
            text = value.encode("utf-16", "surrogatepass").decode("utf-16")
        except UnicodeError:
            text = value
        return text.encode("utf-8", "replace").decode("utf-8").replace("\x00", "")
